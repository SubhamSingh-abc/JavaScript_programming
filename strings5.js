// str.trim();   (Removes spaces from : Start , End)
// trim() removes: spaces " " , tabs \t , newlines \n
// str.trimEnd();     (trims from end)
// str.trimStart();   (trim from start)
let str = "   subham ";
let after_trim_str = str.trim();
console.log(str);
console.log(after_trim_str);
console.log(str.trimStart());      // trim from start
console.log(str.trimEnd());        // trim from end